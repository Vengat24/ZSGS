
public class Main
{
	public static void main(String[] args) {
	    int num =9;
	    for(int j=0;j<num;j++){
	        for(int k=0;k<j;k++){
	            System.out.print(" ");
	        }
	        for(int i=j;i<num;i++){
	            
	            System.out.print("* ");
	        }
	        System.out.println("");
	    }
	}
}
